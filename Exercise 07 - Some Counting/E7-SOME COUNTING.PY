## Exercise 7: Some Counting - 20 Marks

# Count up from 0 to 50 in increments of 1
print("Count up from 0 to 50:")
for i in range(0, 51):
    print(i)

print("\nCount down from 50 to 0:")
# Count down from 50 to 0 in decrements of 1
for i in range(50, -1, -1):
    print(i)

print("\nCount up from 30 to 50:")
# Count up from 30 to 50 in increments of 1
for i in range(30, 51):
    print(i)

print("\nCount down from 50 to 10 in steps of 2:")
# Count down from 50 to 10 in decrements of 2
for i in range(50, 9, -2):
    print(i)

print("\nCount up from 100 to 200 in steps of 5:")
# Count up from 100 to 200 in increments of 5
for i in range(100, 201, 5):
    print(i)